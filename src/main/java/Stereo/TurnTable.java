package Stereo;

public class TurnTable {

    private String needleStatus;
    private int speed;

    public TurnTable() {
        this.needleStatus = needleStatus;
        this.speed = speed;
    }

    public String getNeedleStatus() {
        return this.needleStatus;
    }

    public void setNeedleStatus(String newNeedleStatus) {
        this.needleStatus = newNeedleStatus;
    }

    public int getSpeed() {
        return this.speed;
    }

    public void setSpeed(int newSpeed) {
        this.speed = newSpeed;
    }

}
